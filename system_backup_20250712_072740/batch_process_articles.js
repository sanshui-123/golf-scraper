        console.log('‚ú® ÊâπÈáèÂ§ÑÁêÜÂÆåÊàêÔºÅ');
        console.log(`üìä Â§ÑÁêÜÁªüËÆ°:`);
        console.log(`   - ËæìÂÖ•ÊñáÁ´†Êï∞: ${urls.length + duplicateUrls.length}`);
        console.log(`   - Ë∑≥ËøáÈáçÂ§ç: ${duplicateUrls.length}`);
