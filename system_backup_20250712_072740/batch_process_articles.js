        fs.writeFileSync(urlMapFile, JSON.stringify(urlMapping, null, 2), 'utf8');
        
        console.log(`‚úÖ ‰øùÂ≠òÂÆåÊàê (${Date.now() - saveStart}ms)\n`);
        
